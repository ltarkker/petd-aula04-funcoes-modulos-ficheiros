# Contagem regressiva

# Criamos uma variável chamada contador com valor inicial de 5
contador = 5

# Criamos um ciclo while que continua enquanto o contador for maior ou igual a 0
while contador >= 0:
    
    # Mostra o valor actual do contador
    print(f"Contagem regressiva: {contador}")
    
    # Diminui 1 ao valor do contador
    contador -= 1
    
# Esta mensagem é mostrada quando o ciclo termina
print("Lançamento!")


