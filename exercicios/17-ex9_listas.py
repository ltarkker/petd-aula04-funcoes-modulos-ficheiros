# Lista de frutas
frutas = ["maçã", "banana"]
print(f"Lista original: {frutas}")

# Adicionar ao final
frutas.append("laranja")
print(f"Lista após append: {frutas}")

# Inserir em uma posição específica
frutas.insert(1, "uva")
print(f"Lista após insert: {frutas}")

# Remover por valor
frutas.remove("banana")
print(f"Lista após remove: {frutas}")

# Remover por índice
frutas.pop(0)
print(f"Lista após pop: {frutas}")

